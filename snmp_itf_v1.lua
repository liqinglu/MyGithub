    --为Proto对象添加一个名为dissector的函数，
    --Wireshark会对每个“相关”数据包调用这个函数
    function p_snmp_itf.dissector(buf,pkt,root) 

		local offset = 0
		local text_len = 0
		local mode = 0
		local buf_len = buf:len()
		
		if pkt.src_port == 60200 then -- message direction
			mode = 0 -- request
		else
			mode = 1 -- response
		end
		
        --root:add会在Packet Details窗格中增加一行协议
        local t = root:add(p_snmp_itf,buf:range(offset))
        --t:add，在Packet Details窗格中增加一行属性，
        --并指定要鼠标点击该属性时Packet Bytes窗格中会选中哪些字节
        --t:add(f_mode)
		local myrequesttype = buf:range(offset,4):uint()
		t:add(f_requesttype,buf:range(offset,4))
		offset = offset + 4
		local mylength = buf:range(offset,4):uint()
		t:add(f_length,buf:range(offset,4))
		offset = offset + 4		
		
		if myrequesttype <= 4 then -- requesttype is [0,4]
			t:add(f_reqid,buf:range(offset,4))
			offset = offset + 4
			t:add(f_area,buf:range(offset,4))
			offset = offset + 4
			t:add(f_location,buf:range(offset,4))
			offset = offset + 4
			t:add(f_column,buf:range(offset,4))
			offset = offset + 4
			
			if mylength > 24 then
				if mode == 1 then -- response
					local myresult = buf:range(offset,4):uint()				
					t:add(f_result,buf:range(offset,4))
					offset = offset + 4
					if offset ~= buf_len then
						t:add(f_datatype,buf:range(offset,4))
						offset = offset + 4
						t:add(f_datalength,buf:range(offset,4))
						offset = offset + 4
						
						text_len = buf_len - offset
						t:add(f_payload,buf:range(offset,text_len))
						offset = offset + text_len									
					end
				else -- request
					t:add(f_datatype,buf:range(offset,4))
					offset = offset + 4
					t:add(f_datalength,buf:range(offset,4))
					offset = offset + 4
					
					text_len = buf_len - offset
					t:add(f_payload,buf:range(offset,text_len))
					offset = offset + text_len				
				end;
			end
		else -- requesttype > 4
			local switch = {
				[5] = function()  --SnmpBsCommStateSet_t
					t:add(f_state,buf:range(offset,4))
					offset = offset + 4
				end,
				[6] = function()  --SnmpTargetAddrSet_t
					t:add(f_type,buf:range(offset,4))
					offset = offset + 4
					t:add(f_index,buf:range(offset,4))
					offset = offset + 4
					t:add(f_domain,buf:range(offset,4))
					offset = offset + 4
					
					text_len = buf_len - offset
					t:add(f_addr,buf:range(offset,text_len))
					offset = offset + text_len
				end,
				[7] = function()  --SnmpTargetAddrTableSet_t
					text_len = buf_len - offset
					t:add(f_payload,buf:range(offset,text_len))
					offset = offset + text_len
				end,
				[8] = function()  --SnmpTargetAddrTableSetRes_t
					t:add(f_result,buf:range(offset,4))
					offset = offset + 4
				end,
				[9] = function()  --SnmpMsgConfdReadyInd_t
					--
				end,
				[10] = function() --SnmpMsgNotificationInd_t
					t:add(f_type,buf:range(offset,4))
					offset = offset + 4
					t:add(f_numvars,buf:range(offset,4))
					offset = offset + 4
					
					text_len = buf_len - offset
					t:add(f_payload,buf:range(offset,text_len))
					offset = offset + text_len
				end,
				[11] = function() --SnmpSysObjectSet_t
					t:add(f_sysobjtype,buf:range(offset,4))
					offset = offset + 4
					t:add(f_size,buf:range(offset,4))
					offset = offset + 4
					
					text_len = buf_len - offset
					t:add(f_payload,buf:range(offset,text_len))
					offset = offset + text_len
				end,
				[12] = function() --SnmpMsgVarBinds_t
					t:add(f_reqid,buf:range(offset,4))
					offset = offset + 4
					t:add(f_location,buf:range(offset,4))
					offset = offset + 4
					t:add(f_size,buf:range(offset,4))
					offset = offset + 4
					
					text_len = buf_len - offset
					t:add(f_payload,buf:range(offset,text_len))
					offset = offset + text_len
				end
			}
			
			local f = switch[myrequesttype]
			if(f) then
				f()
			else
				--print "default."
			end
		end
    end

    --这个是获得tcp协议的DissectorTable，并且以端口号排列
    local tce_port_table = DissectorTable.get("tcp.port")

    --为TCP的60200端口注册这个Proto对象，表明这个插件是解析60200端口的snmp消息
    --当遇到源或目的为TCP60200的数据包，就会调用上面的p_snmp_itf.dissector函数
    tce_port_table:add(60200,p_snmp_itf)
